package AppOrder;

public class Item {
	 private String stname;
	    private double stprice;

	    public Item(String stname, double stprice) {
	        this.stname = stname;
	        this.stprice = stprice;
	    }

	    public String getSTname() {
	        return this.stname;
	    }

	    public double getSTprice() {
	        return this.stprice;
	    }
}


